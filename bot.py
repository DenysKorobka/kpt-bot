def load_data() -> dict:
    if os.path.exists(DATA_FILE):
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}

def save_data(data: dict):
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def get_user(data: dict, user_id: int) -> dict:
    uid = str(user_id)
    if uid not in data:
        data[uid] = {
            "current_day": 0,
            "completed_days": [],
            "day_start_times": {},   # {day_num: ISO timestamp коли завершили}
            "started_at": None,
        }
    return data[uid]

def can_access_day(user: dict, day_num: int) -> tuple[bool, str]:
    """
    Повертає (True, "") якщо день доступний,
    або (False, "скільки годин залишилось") якщо ні.
    """
    if day_num == 1:
        return True, ""

    prev_day = str(day_num - 1)
    completed = user.get("completed_days", [])

    if (day_num - 1) not in completed:
        return False, f"Спочатку потрібно завершити День {day_num - 1}."

    prev_time_str = user.get("day_start_times", {}).get(prev_day)
    if not prev_time_str:
        return True, ""

    prev_time = datetime.fromisoformat(prev_time_str)
    now = datetime.now()
    unlock_time = prev_time + timedelta(hours=24)

    if now >= unlock_time:
        return True, ""

    remaining = unlock_time - now
    hours = int(remaining.total_seconds() // 3600)
    minutes = int((remaining.total_seconds() % 3600) // 60)
    return False, f"⏳ День {day_num} відкриється через {hours} год {minutes} хв.\n\nПовторення матеріалу попередніх днів — найкраще використання цього часу! 💪"


